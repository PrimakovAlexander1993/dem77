package com.example.demo.model;

import javax.persistence.Entity;

@Entity
public class Nobody {
    private int id;
    private String name;

    public Nobody(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
